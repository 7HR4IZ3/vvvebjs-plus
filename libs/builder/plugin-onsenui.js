class OnsenuiPlugin extends Plugin {
  initialize() {
    this.builder.config.components.push(new OnsenuiComponents());
    this.builder.on("builder:iframe.loaded", (event, doc) => {
      let style = $(
        `<link href="/css/onsenui.min.css" rel="stylesheet"></link>`
      );
      let style2 = $(
        `<link href="/css/onsen-css-components.min.css" rel="stylesheet"></link>`
      );
      let script = $(`<script src="/libs/builder/onsenui.min.js"></script>`);
      doc.find("head").append(style).append(style2).append(script);
    });
  }
}
